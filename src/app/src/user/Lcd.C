/*************************************************************
** 函数名称: Lcd_CheckCurStatus
** 功能描述: 
** 入口参数: 无
** 出口参数: 无
** 返回参数: 无
** 全局变量: 
** 调用模块: 
*************************************************************/	
unsigned char Lcd_CheckAlarmStatus(void)
{
    unsigned long Iostatus,preIostatus;
    static unsigned char sucCnt=0;
    unsigned char i;
    Iostatus=Io_ReadAlarm();    
    preIostatus = s_stLcdAttrib.preAlarmStatus;
    s_stLcdAttrib.preAlarmStatus=Iostatus;
    if(Iostatus != preIostatus)
    {
        sucCnt =0;
        for(i=0; i<32; i++)
        {
            if((0==PUBLIC_CHECKBIT(preIostatus, i))&&PUBLIC_CHECKBIT(Iostatus, i))
            {
                return i+1;
            }
        }
        ///////////////////////////
        for(i=0; i<32; i++)
        {
            if(Io_ReadAlarmBit((ALARM_BIT)i))
            {
                return i+1;
            }
        }
    }
    else
    {
        if(sucCnt<4)
        {
            sucCnt++;
            return s_stLcdAttrib.alarmindex;
        }
        else
        if(sucCnt==4)
        {
            sucCnt = 10;
            for(i=0; i<32; i++)
            {
                if(Io_ReadAlarmBit((ALARM_BIT)i))
                {
                    return i+1;
                }
            }
        }
        else
        {
            return s_stLcdAttrib.alarmindex;
        }
    }
    ///////////////////
    return 0;
}
