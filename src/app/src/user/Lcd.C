/*********************************************************************
//函数名称	:Lcd_DisplaySystemTest
//功能		:测试菜单子菜单
//输入		:
//输出		:
//使用资源	:
//全局变量	:   
//调用函数	:
//中断资源	:  
//返回		:
//备注		:
*********************************************************************/
void Lcd_DisplaySystemTest(unsigned char type)
{
    u8    Buffer[100];
    u16   length;
    u8 channel = CHANNEL_DATA_1|CHANNEL_DATA_2;
    ProtocolACK ACK_val;
    static u8 count = 0;
    switch(type)
    {
        case MENU_TEST_REGISTER://终端注册
            ACK_val=RadioProtocol_TerminalRegister(channel);   
            break;
        case MENU_TEST_QUALIFY://终端鉴权
            ACK_val=RadioProtocol_TerminalQualify(CHANNEL_DATA_1);//只能一个一个通道发，因为通道不一样鉴权码也不一样      
            ACK_val=RadioProtocol_TerminalQualify(CHANNEL_DATA_2);
            break;
        #ifdef MENU_TEST_LOGOUT
        case MENU_TEST_LOGOUT://终端注销
            ACK_val= RadioProtocol_TerminalLogout(channel);
            if(ACK_OK ==ACK_val)
            {
                if(MyTimerTask[TIME_DELAY_TRIG].TaskTimerState == DISABLE)
                {
                    u8	i;
                    DelayAckCommand = 0x0003; 
                    //触发延时任务
                    SetTimerTask(TIME_DELAY_TRIG, SECOND);
                    //擦除鉴权码
                    ClearTerminalAuthorizationCode(CHANNEL_DATA_1);
                    ClearTerminalAuthorizationCode(CHANNEL_DATA_2);
                }
            }            
            break;
        #endif
        case MENU_TEST_SEND_POST://位置信息汇报
            Public_GetCurBasicPositionInfo(Buffer);
            length = 28;
            ACK_val=RadioProtocol_PostionInformationReport(channel,Buffer,length); 
            break;
        case MENU_TEST_EVENT_REPORT://事件报告
            ACK_val=RadioProtocol_EvenReport(channel,0x01);            
            break;
        case MENU_TEST_SEND_MESSAGE://信息点播
            ACK_val= RadioProtocol_InforamtionOrder(channel,1, 1);            
            break;
        case MENU_TEST_CANCEL_MESSAGE://信息取消
            ACK_val= RadioProtocol_InforamtionOrder(channel,1, 0);            
            break;
        case MENU_TEST_SEND_E_WAYBILL://电子运单上报
            //电子运单上报
            memcpy(Buffer+4,"电子运单",8);
            Buffer[0] = 0;
            Buffer[1] = 0;
            Buffer[2] = 0;
            Buffer[3] = 8;
            ACK_val= RadioProtocol_ElectricReceiptReport(channel,Buffer, 12);
            break;
        case MENU_TEST_DRIVER_INFO://驾驶员身份信息
            count++;
            length = 0;
            // Buffer[length] = 0x07;
            length++;
            // Buffer[length] = 0x02;
            length++;
            //驾驶员姓名长度
            Buffer[length] = 10;
            length++;
            //驾驶员姓名
            memcpy(Buffer+length,"驾驶员姓名",10);
            length += 10;
            //驾驶证号码
            memcpy(Buffer+length,"驾驶证号码1234567890",20);
            length += 20;
            //从业资格证编码
            memcpy(Buffer+length,"从业资格证编码abcdefghijklmnopqrstuvwxyz",40);
            length += 40;
            //发证机构长度
            Buffer[length] = 8;
            length++;
            //发证机构
            memcpy(Buffer+length,"发证机构",8);
            length += 8;
            //签到签退标志,1为签到,2为签退
            if(2 == count%2+1)
            {
                Buffer[0] = 0x00;
                Buffer[1] = 0x01;
            }
            else
            {
                Buffer[0] = 0x00;
                Buffer[1] = 0x02;
            }
            //当前时间 
            Public_ConvertNowTimeToBCDEx(&Buffer[length]);                      
            length+=6;
            //通过透传指令上传驾驶员信息
            ACK_val=  RadioProtocol_OriginalDataUpTrans(channel,0xe3, Buffer, length);            
            break;
        case MENU_TEST_MULIT_EVENT://上发多媒体事件
            ACK_val=RadioProtocol_MultiMediaEvenReport(channel,0x01, 0, 0, 1, 1);
            break;
        case MENU_TEST_SEND_DTU_DATA://数据上行透传
            ACK_val= RadioProtocol_OriginalDataUpTrans(channel,1, Buffer, 10);
            break;     
				case MENU_TEST_RESUME_FCT://恢复出厂未使用状态
            ACK_val = E2ParamApp_ResumeFactory();
						//重启
			 			NVIC_SystemReset();
            break;   
        default:
            break;
    }
    ///////////////////////////
    if(ACK_OK == ACK_val)
    {
        //显示发送成功
        LcdShowStr(0, 0, "发送成功！ ", 10);
    }
    else
    {
        //显示发送失败
        LcdShowStr(0, 0, "发送失败！ ", 10);
        
    }
}
